    private void PendingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PendingActionPerformed
      if (!isPendingViewLoaded) {

    try {
        String sql = "SELECT OrderID, CustomerID, ProductName, Quantity, TotalPrice, PaymentMethod, DeliveryAddress, OrderDate FROM view_pending_orders";
        try (PreparedStatement stmt = conn.prepareStatement(sql)) {
            try (ResultSet rs = stmt.executeQuery()) {
                DefaultTableModel model = new DefaultTableModel(
                    new String[]{"Order ID", "Customer ID", "Product Name", "Quantity", "Total Price", "Payment Method", "Delivery Address", "Order Date"}, 0
                );
                jTable1.setModel(model);

                while (rs.next()) {
                    Object[] row = new Object[]{
                        rs.getInt("OrderID"),
                        rs.getInt("CustomerID"),
                        rs.getString("ProductName"),
                        rs.getInt("Quantity"),
                        rs.getDouble("TotalPrice"),
                        rs.getString("PaymentMethod"),
                        rs.getString("DeliveryAddress"),
                        rs.getDate("OrderDate")
                    };
                    model.addRow(row);
                }

                isPendingViewLoaded = true;
                JOptionPane.showMessageDialog(this, "Pending orders loaded. Click again to confirm shipment.");
            }
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading pending orders.");
    }

} else {
    
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select an order to mark as 'Shipped'.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Move this order to 'Shipped'?", "Update Status", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        int orderId = (int) jTable1.getValueAt(selectedRow, 0);
        try {
            String updateSql = "UPDATE orders SET Status = 'Shipped' WHERE OrderID = ?";
            try (PreparedStatement updateStmt = conn.prepareStatement(updateSql)) {
                updateStmt.setInt(1, orderId);
                int rowsAffected = updateStmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Order status updated to 'Shipped'.");

                    
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(selectedRow);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update order status.");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating order status.");
        }
    }

   
    isPendingViewLoaded = false;
}

    }//GEN-LAST:event_PendingActionPerformed

